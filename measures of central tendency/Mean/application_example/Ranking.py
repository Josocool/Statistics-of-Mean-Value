import pandas as pd

# สร้าง DataFrame ของนักเรียนและคะแนนแต่ละคน
data = {
    "ชื่อ": ["นักเรียน A", "นักเรียน B", "นักเรียน C", "นักเรียน D"],
    "คณิตศาสตร์": [85, 78, 92, 88],
    "วิทยาศาสตร์": [90, 82, 89, 85],
    "ภาษาไทย": [78, 75, 88, 90],
    "ภาษาอังกฤษ": [92, 88, 85, 80],
    "สังคม": [88, 80, 91, 86],
}

df = pd.DataFrame(data)

# คำนวณคะแนนเฉลี่ยของแต่ละนักเรียน
df["คะแนนเฉลี่ย"] = df.iloc[:, 1:].mean(axis=1)

# จัดอันดับจากมากไปน้อย
df = df.sort_values(by="คะแนนเฉลี่ย", ascending=False)

print(df)
